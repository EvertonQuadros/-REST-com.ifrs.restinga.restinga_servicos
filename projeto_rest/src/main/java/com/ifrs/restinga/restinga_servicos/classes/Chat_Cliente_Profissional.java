package com.ifrs.restinga.restinga_servicos.classes;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.JoinColumn;
import javax.persistence.OneToOne;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

/**
 *
 * @author not275ssd
 */
@Entity
@Table(name = "chat_cliente_profissional")
public class Chat_Cliente_Profissional extends Entidade {

    @OneToOne
    @JoinColumn(name = "cliente_id")
    private Pessoa cliente;
    
    @OneToOne
    @JoinColumn(name = "profissional_id")
    private Pessoa profissional;
    
    @Column(name = "dataEnvio", columnDefinition="DATETIME")
    @Temporal(TemporalType.TIMESTAMP)
    private Date dataEnvio;
    
    private String texto;
    private String imagem;
    
    @Override
    public int getId() {
        return id;
    }

    @Override
    public void setId(int id) {
        this.id = id;
    }

    public Pessoa getCliente() {
        return cliente;
    }

    public void setCliente(Pessoa cliente) {
        this.cliente = cliente;
    }

    public Pessoa getProfissional() {
        return profissional;
    }

    public void setProfissional(Pessoa profissional) {
        this.profissional = profissional;
    }

    public Date getDataEnvio() {
        return dataEnvio;
    }

    public void setDataEnvio(Date dataEnvio) {
        this.dataEnvio = dataEnvio;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    public String getImagem() {
        return imagem;
    }

    public void setImagem(String imagem) {
        this.imagem = imagem;
    }
    
}
